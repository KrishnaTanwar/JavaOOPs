public class for_while_do_while {
    public static void main(String[] args) {
        for(int i = 11; i<10;i++){
            System.out.println("Krishna");
        }
        int i = 11;

        while(i<10){
            System.out.println("Krishna");

        }

        do{
            System.out.println("Krishna");
            i++;

        }

        while(i<10);
    }
    
}
