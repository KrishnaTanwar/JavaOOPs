public class table_number_using_function {
    public static void main(String[] args) {
        int n = 5;
       table_num(n);
    }
    public static void table_num(int a) {
        for(int i = 1; i<=10;i++){
            System.out.println(a+'*'+i+'='+a*i);
            
        }
        return;
    }
        
    }
    

